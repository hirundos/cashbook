package com.example.dkdus.cashbook.model;


public class ChildList {

    public String type;
    public String contents;
    public String money;
    public String date;
    public String category;
    public String time;

    public ChildList(String type, String contents, String money, String date, String category, String time) {
        this.type = type;
        this.contents = contents;
        this.money = money;
        this.date = date;
        this.category = category;
        this.time = time;
    }

}
